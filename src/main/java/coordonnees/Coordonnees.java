package coordonnees;

public class Coordonnees {
    private int abscisses;
    private int ordonnees;

    public Coordonnees(int abscisses, int ordonnees){
        this.abscisses = abscisses;
        this.ordonnees = ordonnees;
    }

    public int getAbscisses() {
        return abscisses;
    }

    public int getOrdonnees() {
        return ordonnees;
    }
}